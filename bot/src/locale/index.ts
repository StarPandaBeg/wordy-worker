export const _ = (key: string, replacements?: Record<string, any>) => {
	if (key in locale) {
		let val = locale[key];
		if (!replacements) return val;
		for (const [key, value] of Object.entries(replacements)) {
			val = val.replace(`%${key}%`, value);
		}
		return val;
	}
	throw new Error(`Unknown locale key: ${key}`);
};
